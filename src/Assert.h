#ifndef _ASSERT_H_
#define _ASSERT_H_

/******************************************************************************
                                     Macros
******************************************************************************/
/*****************************************************************************/
#define ASSERT( _cond ) { \
    if ( !(_cond) ) { \
        Assert_func( __func__, __LINE__ ); \
    } \
}


/******************************************************************************
                                   Functions
******************************************************************************/
/*****************************************************************************/
void
Assert_func( const char* func, unsigned int line );


#endif /* _ASSERT_H_ */
